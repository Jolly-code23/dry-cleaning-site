<hr /><p class="style3"><u>To begin with, what is Dry Cleaning?</u></p>
<p class="style11">Dry Cleaning is a type of cleaning that involves the use of solvents other than water. The solvents can be chemicals such as Tetracholoroethane, <strong>Tricholoroethane </strong>or Petroleumspirits. Typically, Dry Cleaning  is similar to a combination of a domestic washing machine and clothes dryer. Garments are placed in the washing or extraction chamber (referred to as the 'basket' or 'drum'), which constitutes the core of the machine. The washing chamber contains a horizontal, perforated drum that rotates within an outer shell. The shell holds the solvent while the rotating drum holds the garment load. The basket capacity is between about 10 and 40&nbsp;kg (22 to 88&nbsp;lb).</p>
<p class="style11">During the wash cycle, the chamber is filled approximately one-third full of solvent and begins to rotate, agitating the clothing. The solvent temperature is maintained at 30&nbsp;degrees Celsius (86&nbsp;degrees Fahrenheit), as a higher temperature may damage it. During the wash cycle, the solvent in the chamber (commonly known as the 'cage' or 'tackle box') is passed through a filtration chamber and then fed back into the 'cage'. This is known as the cycle and is continued for the wash duration. The solvent is then removed and sent to a distilation unit consisting of a boiler and condenser. The condensed solvent is fed into a separator unit where any remaining water is separated from the solvent and then fed into the 'clean solvent' tank. The ideal flow rate is roughly 8 liters of solvent per kilogram of garments per minute, depending on the size of the machine. </p>
